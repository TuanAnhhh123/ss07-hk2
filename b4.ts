function printTypeOfArgument(arg: any): void {
    console.log("Kiểu dữ liệu của đối số là:", typeof arg);
}

// Sử dụng hàm với các đối số khác nhau
printTypeOfArgument(5); // In ra: Kiểu dữ liệu của đối số là: number
printTypeOfArgument("Hello"); // In ra: Kiểu dữ liệu của đối số là: string
printTypeOfArgument(true); // In ra: Kiểu dữ liệu của đối số là: boolean
